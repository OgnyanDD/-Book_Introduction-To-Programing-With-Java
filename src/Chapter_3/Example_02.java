package Chapter_3;

public class Example_02 {
    public static void main(String[] args) {
        int x = 10, y = 5;

        System.out.println("x > y : " + (x > y)); // true
        System.out.println("x < y : " + (x < y)); // false
        System.out.println("x >= y : " + (x >= y)); // true
        System.out.println("x <= y : " + (x <= y)); // false
        System.out.println("x == y : " + (x == y)); // false
        System.out.println("x != y : " + (x != y)); // true
    }
}
